package gov.nasa.ensemble.common.ui.gef;

public interface SplitConstants {
	
	/**
     * Vertical split indicates the <code>Figure</code>s are
     * split along the y axis.  For example the two
     * <code>Figure</code>s will be split one on top of the other.
     */
    public final static int VERTICAL_SPLIT = 0;

    /**
     * Horizontal split indicates the <code>Figure</code>s are
     * split along the x axis.  For example the two
     * <code>Figure</code>s will be split one to the left of the
     * other.
     */
    public final static int HORIZONTAL_SPLIT = 1;

}
