package gov.nasa.ensemble.resources;

import org.eclipse.core.runtime.Plugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class EnsembleResourcesPlugin extends Plugin {

	// The plug-in ID
	public static final String PLUGIN_ID = "gov.nasa.ensemble.resources";

	// The shared instance
	private static EnsembleResourcesPlugin plugin;

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.core.runtime.Plugins#start(org.osgi.framework.BundleContext)
	 */
	@Override
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.core.runtime.Plugin#stop(org.osgi.framework.BundleContext)
	 */
	@Override
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 *
	 * @return the shared instance
	 */
	public static EnsembleResourcesPlugin getDefault() {
		return plugin;
	}

	public static String getTestNatureId() {
		return PLUGIN_ID + ".testNature";
	}
	
	public static String getTestBuilderId() {
		return PLUGIN_ID + ".testBuilder";
	}
}
